#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int	f;

	f = nb;
	if (nb < 0)
	{
		ft_putchar('-');
		f = f * (-1);
	}
	if (f > 9)
	{
		ft_putnbr(f / 10);
	}
	ft_putchar(48 + f % 10);
}
