#include "libft.h"

void	ft_printbits(unsigned char octet)
{
	int		i;
	char	c;

	i = 128;
	while (i > 0)
	{
		if (octet < i)
		{
			c = '0';
			i /= 2;
			write(1, &c, 1);
		}
		else
		{
			c = '1';
			i /= 2;
			write(1, &c, 1);
		}
	}
}
